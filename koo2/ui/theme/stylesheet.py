def apply_to_app(
    app: object,
    palette: Palette = DEFAULT_LIGHT,
    typography: Typography = DEFAULT_TYPOGRAPHY,
) -> None:
    """Apply the Material Design stylesheet to a ``QApplication``."""
    app.setStyleSheet(build_stylesheet(palette, typography))  # type: ignore[attr-defined]
