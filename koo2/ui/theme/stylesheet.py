"""
QSS stylesheet generator for Material Design 3.

Generates a Qt Style Sheet string from a :class:`~koo2.ui.theme.palette.Palette`
and :class:`~koo2.ui.theme.typography.Typography` instance, giving all
standard Qt widgets an MD3-consistent look without external CSS frameworks.

Usage::

    from koo2.ui.theme.palette import DEFAULT_LIGHT
    from koo2.ui.theme.typography import DEFAULT_TYPOGRAPHY
    from koo2.ui.theme.stylesheet import build_stylesheet

    app.setStyleSheet(build_stylesheet(DEFAULT_LIGHT, DEFAULT_TYPOGRAPHY))
"""
from __future__ import annotations

from koo2.ui.theme.palette import DEFAULT_LIGHT, Palette
from koo2.ui.theme.typography import DEFAULT_TYPOGRAPHY, Typography


def _hex_with_alpha(hex_color: str, alpha: float) -> str:
    """Convert ``#RRGGBB`` + float alpha → ``rgba(r,g,b,a)``."""
    h = hex_color.lstrip("#")
    r, g, b = int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)
    return f"rgba({r}, {g}, {b}, {alpha:.2f})"


